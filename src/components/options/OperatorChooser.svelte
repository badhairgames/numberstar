<script lang="ts">
    import { createEventDispatcher } from "svelte";
    import RoundButton from "../buttons/RoundButton.svelte";
    export let operators: string[] = [];
    const dispatch = createEventDispatcher();

    function updateValues(event) {
        const selected = event.detail.selected;
        const value = event.detail.text;
        const included = operators.includes(value);
        if (selected && !included) {
            operators.push(value);
        } else if (included) {
            const index = operators.indexOf(value, 0);
            if (index > -1) {
                operators.splice(index, 1);
            }
        }

        dispatch('select', { operator: value });
    }
</script>

<div class="container">
    <RoundButton text="+" keyCode="49" class="btn" on:select={updateValues}></RoundButton>
    <RoundButton text="-" keyCode="50" class="btn" on:select={updateValues}></RoundButton>
    <RoundButton text="ร" keyCode="51" class="btn" on:select={updateValues}></RoundButton>
    <RoundButton text="รท" keyCode="52" class="btn" on:select={updateValues}></RoundButton>
</div>

